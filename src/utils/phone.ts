// Clean up double-escaped backslashes in regex patterns at runtime (common issue in auto-generated/JSON-imported patterns)
export const phonePatterns = rawPhonePatterns.map((entry) => {
  if (entry.pattern.source.includes("\\\\")) {
    try {
      return {
        ...entry,
        pattern: new RegExp(entry.pattern.source.replace(/\\\\/g, "\\")),
      };
    } catch {
      // fallback
    }
  }
  return entry;
});

/**
 * Validates a phone number against the known country patterns.
 * Returns true if the number matches any country pattern.
 */
export const isValidPhoneByPattern = (phoneNumber: string | undefined | null) => {
  if (!phoneNumber) return false;
  const normalized = String(phoneNumber).replace(/\s/g, '');
  return phonePatterns.some((entry) => entry.pattern.test(normalized));
};
